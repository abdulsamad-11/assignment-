var firstNum = Number(prompt("FirstNumber plz"));
var secondNum = Number(prompt("Second Number plz"));
var operation = prompt("operation");

if (operation == "+"){
    alert(firstNum+secondNum);
}

if (operation == "-"){
    alert(firstNum-secondNum);
}

if (operation == "*"){
    alert(firstNum*secondNum);
}

if (operation == "/"){
    alert(firstNum/secondNum);
}

if (operation == "%"){
    alert(firstNum%secondNum);
}










